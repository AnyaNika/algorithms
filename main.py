# палиндром — это строка, которая читается одинаково слева направо и справа налево
# шаг 1: переводим строку в один регистр (чтобы не учитывать разницу между заглавными и строчными буквами)
# шаг 2: удаляем все пробелы (чтобы фраза с пробелами тоже могла быть палиндромом)
# шаг 3: сравниваем строку с её "перевёрнутой" версией
# шаг 4: если они равны — возвращаем True (строка палиндром)
# шаг 5: если не равны — возвращаем False (строка не палиндром)

def is_palindrome(text):
    # шаг 1: приводим строку к нижнему регистру
    text = text.lower()

    # шаг 2: убираем пробелы
    text = text.replace(" ", "")

    # шаг 3: сравниваем строку с её перевёрнутой версией
    if text == text[::-1]:
        return True
    else:
        return False


# Примеры проверки
print(is_palindrome("level"))  # True
print(is_palindrome("Racecar"))  # True
print(is_palindrome("hello"))  # False
print(is_palindrome("А роза упала на лапу Азора"))  # True